# Dictionary mapping disclaimer types to content
DISCLAIMER_CONTENT = {
    'resume_analysis': RESUME_ANALYSIS_DISCLAIMER,
    'job_matching': JOB_MATCHING_DISCLAIMER,
    'data_processing': DATA_PROCESSING_DISCLAIMER,
    'ai_limitations': AI_LIMITATIONS_DISCLAIMER,
    'privacy_policy': PRIVACY_POLICY,
    'terms_of_service': TERMS_OF_SERVICE,
}

def get_disclaimer_content(disclaimer_type: str) -> str:
    """Get disclaimer content by type"""
    return DISCLAIMER_CONTENT.get(disclaimer_type, "")

def create_default_disclaimers():
    """Create default disclaimers in the database"""
    from .models import LegalDisclaimer
    from datetime import date
    
    for disclaimer_type, content in DISCLAIMER_CONTENT.items():
        LegalDisclaimer.objects.get_or_create(
            disclaimer_type=disclaimer_type,
            defaults={
                'title': f"{disclaimer_type.replace('_', ' ').title()}",
                'content': content,
                'version': '1.0',
                'is_active': True,
                'effective_date': date.today(),
                'requires_consent': disclaimer_type in ['resume_analysis', 'job_matching', 'data_processing']
            }
        )

